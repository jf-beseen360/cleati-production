def generate_files(output_dir: str = None):
    """Generate all translation files."""
    if output_dir is None:
        output_dir = os.path.dirname(__file__)

    translations_dir = os.path.join(output_dir, 'translations')

    for lang, namespaces in TRANSLATIONS.items():
        lang_dir = os.path.join(translations_dir, lang)
        os.makedirs(lang_dir, exist_ok=True)

        for namespace, translations_data in namespaces.items():
            file_path = os.path.join(lang_dir, f'{namespace}.json')
            with open(file_path, 'w', encoding='utf-8') as f:
                json.dump(translations_data, f, ensure_ascii=False, indent=2)
            print(f"Generated: {file_path}")


if __name__ == '__main__':
    generate_files()
    print("All translation files generated successfully!")
